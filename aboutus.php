      <h1>About Hospital</h1>
      <p align="justify">Dr.Care Hospitals was established in 1983 by Dr. Prathap C Reddy, renowned as the architect of modern healthcare in India. As the nation's first corporate hospital, Dr.Care Hospitals is acclaimed for pioneering the private healthcare revolution in the country.

Dr.Care has emerged as Asia's foremost integrated healthcare services provider and has a robust presence across the healthcare ecosystem, including Hospitals, Pharmacies, Primary Care & Diagnostic Clinics and several retail health models. The Group also has Telemedicine units across 10 countries, Health Insurance Services, Global Projects Consultancy, Medical Colleges, Medvarsity for E-Learning, Colleges of Nursing and Hospital Management and a Research Foundation. In addition, 'ASK Dr.Care' - an online consultation portal and Dr.Care Home Health provide the care continuum.

The cornerstones of Dr.Care legacy are its unstinting focus on clinical excellence, affordable costs, technology and forward-looking research & academics. Dr.Care Hospitals was among the first few hospitals in the world to leverage technology to facilitate seamless healthcare delivery. The organization embraced rapid advancement in medical equipments worldwide, and pioneered the introduction of several cutting edge innovations in India. Recently, South East Asia's first Proton Therapy Centre commenced operations at the Dr.Care Proton Cancer Centre in Chennai.

Since inception, Dr.Care Hospitals has been honoured by the trust of over 150 million patients who came from 140 countries. At the core of Dr.Care patient-centric culture is TLC (Tender Loving Care), the magic that inspires hope amongst its patients.

As a responsible corporate citizen, Dr.Care Hospitals takes the spirit of leadership well beyond business and has embraced the responsibility of keeping India healthy. Recognizing that Non Communicable Diseases (NCDs) are the greatest threat to the nation, Dr.Care Hospitals is continuously educating its fellow Indians on preventive healthcare as the key to wellness. Likewise, envisioned by Dr. Prathap C Reddy, the "Billion Hearts Beating Foundation" endeavours to keep Indians heart-healthy.

Dr.Care Hospitals has championed numerous social initiatives - citing a few would include "Save a Child's Heart Initiative (SACHi) which screens and provides paediatric cardiac care to underprivileged children ailing with congenital heart diseases, SAHI (Society to Aid the Hearing Impaired) and the CURE Foundation focused on cancer care also assist children from financially challenged homes. To introduce population health into the Indian narrative, the Total Health Chennai Dr.Care Hospitals Foundation, as envisaged by Dr. Reddy, is piloting a unique model of healthcare in the Thavanampalle Mandal of Andhra Pradesh. It aims to provide "holistic healthcare" for the entire community starting from birth, through one's journey into childhood, adolescence, adulthood and old age.

